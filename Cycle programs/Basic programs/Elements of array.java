public class Array {  
    public static void main(String[] args) {  
        int [] arr = new int [] {5, 4, 23, 14, 55};  
        System.out.println("Elements of given array: ");    
        for (int i = 0; i < arr.length; i++) {  
            System.out.print(arr[i] + " ");  
        }  
    }  
}
